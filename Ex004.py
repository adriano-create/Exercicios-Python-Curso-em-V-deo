n = input("Digite Algo ")

print('É um número? ', n.isalnum())
print('É uma letra? ', n.isalpha())
print('Tem letras e/ou números? ', n.isalnum())
print('Está somente em letras maiusculas? ', n.isupper())
print('Está somente em letras minusculas? ', n.islower())
print('É somente espaços? ', n.isspace())
print('Está captalizada? ', n.istitle())
